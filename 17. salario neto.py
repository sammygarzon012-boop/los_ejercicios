# calcular salario neto de un trabajador  

  

salario_bruto = float(input("Ingrese el salario bruto del trabajador: ")) 

descuentos = float(input("Ingrese el valor total de los descuentos: ")) 

  

salario_neto = salario_bruto - descuentos 

  

print(f"El salario neto del trabajador es: {salario_neto}") 